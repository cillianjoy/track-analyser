def _create_waveform(sr: int = 22050, duration: float = 0.1) -> np.ndarray:
    t = np.linspace(0.0, duration, int(sr * duration), endpoint=False)
    left = 0.25 * np.sin(2 * np.pi * 220.0 * t)
    right = 0.4 * np.sin(2 * np.pi * 330.0 * t)
    return np.stack([left, right], axis=-1)


def _write_wav(tmp_path: Path, filename: str, subtype: str) -> Path:
    samples = _create_waveform()
    sr = 22050
    path = tmp_path / filename
    sf.write(path, samples, sr, subtype=subtype)
    return path


def _write_mp3(tmp_path: Path) -> Path:
    binary = zlib.decompress(base64.b64decode("".join(MP3_FIXTURE_Z_BASE64.split())))
    path = tmp_path / "sine.mp3"
    path.write_bytes(binary)
    return path


@pytest.mark.parametrize(
    ("subtype", "filename"),
    [
        pytest.param("PCM_16", "sine_pcm16.wav", id="pcm16"),
        pytest.param("PCM_24", "sine_pcm24.wav", id="pcm24"),
        pytest.param("FLOAT", "sine_float32.wav", id="float32"),
    ],
)
def test_load_audio_wav_variants(tmp_path: Path, subtype: str, filename: str) -> None:
    path = _write_wav(tmp_path, filename, subtype=subtype)
    expected, sr = sf.read(path, dtype="float32", always_2d=True)
    expected = expected.T

    samples, loaded_sr, metadata = load_audio(path, mono=False)

    assert loaded_sr == sr
    assert samples.dtype == np.float32
    assert samples.shape == expected.shape
    assert np.allclose(samples, expected, atol=1e-6)

    assert metadata["file_type"] == "WAV"
    assert metadata["channels"] == expected.shape[0]
    assert metadata["duration"] == pytest.approx(expected.shape[-1] / sr, rel=1e-6)
    assert metadata.get("subtype") is not None


def test_load_audio_mp3_metadata(tmp_path: Path) -> None:
    path = _write_mp3(tmp_path)

    samples, sr, metadata = load_audio(path, mono=False)
    reference, ref_sr = sf.read(path, dtype="float32", always_2d=True)
    reference = reference.T

    assert sr == ref_sr
    assert samples.shape == reference.shape
    assert np.allclose(samples, reference, atol=1e-5)

    assert metadata["file_type"] == "MP3"
    assert metadata["channels"] == reference.shape[0]
    assert metadata["duration"] == pytest.approx(reference.shape[-1] / sr, rel=1e-5)
    assert metadata.get("subtype")


def test_load_audio_failure_descriptive(monkeypatch: pytest.MonkeyPatch, tmp_path: Path) -> None:
    from track_analyser import io

    monkeypatch.setattr(io, "sf", None)

    def _raise(*_args, **_kwargs):
        raise io.audioread.NoBackendError()

    monkeypatch.setattr(io.audioread, "audio_open", _raise)

    missing = tmp_path / "missing.mp3"
    missing.write_bytes(b"")

    with pytest.raises(RuntimeError) as excinfo:
        io.load_audio(missing)

    assert "Could not decode audio file" in str(excinfo.value)
